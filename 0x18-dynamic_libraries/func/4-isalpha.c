#include "main.h"
#include <stdio.h>
#include <stdlib.h>
#include <ctype.h>
#include <string.h>

/**
 * _isalpha - This checks for an alphabetic character.
 * @c: The character to be checked.
 *
 * Return: 1 if the character is an
 * alphabetic character (either uppercase
 * or lowercase), 0 otherwise.
 */
int _isalpha(int c)
{
return (isalpha(c));
}
